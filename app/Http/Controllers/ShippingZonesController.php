    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $zone=ShippingZones::find($id);
        $zone->delete();

        return redirect('/admin/shipping-zones')->with('success', 'Zone Deleted Successfully');
    }
