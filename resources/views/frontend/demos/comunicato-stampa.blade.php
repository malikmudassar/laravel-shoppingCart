                                    <h3>15 marzo 2018.  Una nuova prospettiva per la didattica della grammatica</h3>
                                    <p>La casa editrice SEI propone, per l'anno scolastico 2018-2019, <b>un innovativo progetto didattico per l'insegnamento della grammatica</b> nella scuola secondaria di primo grado, con la collaborazione di Schooltoon.</p>

                                    <p>Il nuovo libro di testo <b>I puntini <i>sulle i</i></b> , degli affermati autori Anna Degani, Anna Maria Mandelli e Pier Giorgio Viberti, si caratterizza da un lato per la <b>chiarezza espositiva</b> e <b>l'altissima operatività</b>, dall'altro per l'apertura a una <b>scuola che cambia</b>, a partire dal meditato uso di <b>nuove metodologie didattiche</b>.</p>

                                    <p>Su questo orientamento si innesta un percorso solido e innovativo per svolgere attività di <b><i>flipped classroom</i></b> non in modo estemporaneo, ma in modo strutturato e affidabile, durante i tre anni di corso. La flipped classroom è una metodologia d'insegnamento che propone di "capovolgere" la classe, ossia di invertire il tradizionale schema di insegnamento e apprendimento: a casa l'alunno esplora un argomento che poi, in classe, viene approfondito e sul quale l'alunno è messo alla prova.</p>

                                    <p>Ciascun capitolo del testo presenta una parte introduttiva in cui si invita lo studente a visionare un <b>cartoon didattico</b> che si propone come una <b>"sit-com educativa"</b>: nella classe del professor Eddie, gli alunni Giovanna, Moretti, Ferrari e i loro compagni si scontrano quotidianamente con verbi, nomi, aggettivi e con tutti gli argomenti di grammatica, lezione dopo lezione. Tra errori, spiegazioni ed esercizi, gli alunni dell'ironico cartoon vivono, mese per mese, lo stesso percorso degli alunni in carne ed ossa che hanno in mano il libro di testo.</p>

                                    <p>Il docente che sceglie di adottare <b>I puntini <i>sulle i</i></b>, dispone, nel volume Le Guide SEI a lui riservato, di indicazioni puntuali per accompagnare gli alunni all'uso di un mezzo, il video, che non può mai essere considerato come un'alternativa alla lezione vera e propria, ma come un'opportunità in più, e che pertanto deve essere attentamente calibrata su ogni classe in cui viene usato.</p>

                                    <p>"Grazie a quest'opera, l'insegnante dispone di un <b>valido sostegno per le più consolidate prassi di insegnamento</b> - dichiara Ulisse Jacomuzzi, amministratore delegato casa editrice SEI - ma egli può anche scegliere di affiancare, costantemente guidato, nuove metodologie didattiche.</p>

                                    <p>Con la flipped classroom, <b>l'aula diventa un vero e proprio laboratorio</b>: non più semplice luogo di trasmissione delle nozioni, ma spazio di lavoro e discussione, dove si impara sperimentando, dove ci si pone in dialogo con la realtà, generando un confronto tra gli alunni, con l'insegnante nel ruolo di guida e moderatore. E si sa: </b>

                                    <blockquote>fare esperienza è sempre il modo più efficace per imparare".</blockquote>

                                    <p>Per tracciare questo percorso, la SEI Editrice ha scelto come partner <b>Schooltoon</b>, una property di Kaderby Media S.r.l, azienda attiva nella Digital Education da più di 5 anni. Schooltoon è una "Scuola a Cartoon", nella quale un gruppo di personaggi animati fa convivere la didattica con la musica, l'arte, l'attualità e la tecnologia, senza mai perdere di vista il rigore necessario a trasmettere correttamente i contenuti scolastici.</p>

                                    <p>"L'ironia e la leggerezza di un cartone animato rendono i contenuti didattici più vicini ai ragazzi, più semplici per gli studenti con <b>Bisogni Educativi Speciali</b>, più accessibili per gli stranieri residenti in Italia", dichiara Antonio Brunacci, CEO di Kaderby Media. "Schooltoon è impegnata da sempre nel supportare <b>l'evoluzione della didattica</b>, nel semplificare l'accesso alla cultura, nel digitalizzare il sapere con un video-storytelling che supporti gli studenti, i genitori e gli insegnanti. Con questo approccio entrano per la prima volta in un manuale scolastico le dinamiche di attualità, facendo percepire la grammatica come parte del quotidiano dei nativi digitali e promuovendo la contaminazione di aree del sapere che in genere restano separate.</p>

                                    <blockquote>Attraverso questa partnership, la SEI Editrice e Schooltoon arrivano diritti al cuore di una scuola che cambia". </blockquote>

